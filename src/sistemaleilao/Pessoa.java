    public void carregaTodosLeiloes() {
        /*
         * Recebe em um ArrayList de Strings, onde cada um contém o código do
         * leilão, o nome do produto e o preço atual, de todos os leilões que estão ativos
         * (verificado por um campo no banco de dados).
         */
        comboTodosLeiloes.removeAllItems(); //Remove todos os itens do comboBox antes de acrescentar
        try {
            ArrayList<String> todosLeiloes = banco.carregaLeiloes();
            for (Iterator i = todosLeiloes.iterator(); i.hasNext();) {
                comboTodosLeiloes.addItem(i.next());
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar os leilões ativos",
                    "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

    public void carregaLeiloesUsuario() {
        /*
         * Tem a mesma função do método carregaTodosLeiloes(), mas, neste
         * caso, carrega apenas os leilões que foram abertos pelo usuário,
         * através da sobrecarga do método carregaLeiloes()
         */
        comboLeiloesUsuario.removeAllItems();
        try {
            ArrayList<String> leiloesUsuario = banco.carregaLeiloes(codLogin);

            for (Iterator i = leiloesUsuario.iterator(); i.hasNext();) {

                comboLeiloesUsuario.addItem(i.next());
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar os leilões ativos",
                    "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

    // Valida todos os campos de texto transformando-os em Strings(método da interface)
    @Override
    public boolean valida(String[] texts) {
        //Verifica se existem campos nulos
        for (int i = 0; i < 5; i++) {
            if (texts[i].length() < 1) {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos", "Erro",
                        JOptionPane.ERROR_MESSAGE);
                return false;
            }
        }
        this.nome = texts[0];
        //Valida tempo de uso
        try {
            this.tempoUso = Integer.parseInt(texts[1]);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Coloque o tempo de uso em meses",
                    "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        //Armazena estado de conservação 
        this.estadoConservacao = texts[2];

        //Valida preço
        try {
            this.preco = Double.parseDouble(texts[3]);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Preço campo numérico",
                    "Erro", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        //Armazena descricao
        this.descricao = texts[4];

        return true;
    }

