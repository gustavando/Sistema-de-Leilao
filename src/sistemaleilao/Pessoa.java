    private void btCadstraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadstraActionPerformed
        texts[0] = txtNome.getText();
        texts[1] = txtTempo.getText();
        texts[2] = txtEstado.getText();
        texts[3] = txtValor.getText();
        texts[4] = txtDescricao.getText();
        /*
         * Se todos os campos de texto forem validados com sucesso, é cadastrado
         * no banco de dados o leilão e as informações do produto relacionado a
         * ele
         */
        if (valida(texts)) {
            try {
                banco.cadastraProduto(codProduto, codLeilao, nome, tempoUso, preco,
                        estadoConservacao, descricao);
                banco.cadastraLeilao(codLeilao, codProduto, codLogin, preco);
                carregaTodosLeiloes();
                carregaLeiloesUsuario();
                JOptionPane.showMessageDialog(null, "Dados cadastrados com sucesso", "Sucesso",
                        JOptionPane.DEFAULT_OPTION);
                /* Após o registro é codProduto e codLeilao recebem +1 para que não
                 * seja necessário o cliente sair do sistema e entrar de novo para que
                 * possa criar outro leilão
                 */
                codProduto++;
                codLeilao++;
                //Todos os campos de texto são zerados
                txtNome.setText("");
                txtTempo.setText("");
                txtEstado.setText("");
                txtValor.setText("");
                txtDescricao.setText("");
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Erro no cadastro do leilão",
                        "Erro", JOptionPane.ERROR_MESSAGE);
            }

        }
    }//GEN-LAST:event_btCadstraActionPerformed

    private void btLanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLanceActionPerformed
        /*
         * Clicar neste botão faz o programa verificar as exceções para os
         * campos de texto, e chama o método verificaLance. Este método registra
         * o usuário como o arrematante da tabela leilão (aquele que até agora
         * deu o maior lance) e muda o lance mínimo)
         */
        int tentativaLeilao = 0;
        double lance = 0;
        try {
            tentativaLeilao = Integer.parseInt(txtNumeroLeilao.getText());
            lance = Double.parseDouble(txtValorLance.getText());

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro, apenas números e campos não-nulos",
                    "Erro", JOptionPane.ERROR_MESSAGE);
        }

        try {
            banco.verificaLance(codLogin, tentativaLeilao, lance);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Erro de SQL ao dar lance",
                    "Erro", JOptionPane.ERROR_MESSAGE);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btLanceActionPerformed

